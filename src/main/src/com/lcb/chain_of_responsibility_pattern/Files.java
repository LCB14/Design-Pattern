package com.lcb.chain_of_responsibility_pattern;

public class Files {
	
    private String fileName;
    private int level;
    
    public String getFileName() {
    	
        return fileName;
    }
    
    public void setFileName(String fileName) {
    	
        this.fileName = fileName;
    }
    
    public int getLevel() {
    	
        return level;
    }
    
    public void setLevel(int level) {
    	
        this.level = level;
    }
}